Console.WriteLine("Digite o primeiro valor : ");
int valor_A = Convert.ToInt32(Console.ReadLine());
Console.WriteLine("Digite o segundo valor : ");
int valor_B = Convert.ToInt32(Console.ReadLine());
Console.WriteLine("Digite o terceiro valor : ");
int valor_C = Convert.ToInt32(Console.ReadLine());
Console.WriteLine("Digite o quarto valor : ");
int valor_D = Convert.ToInt32(Console.ReadLine());

Console.Write("Valor Somatorio");
Console.Write(valor_A + valor_B);
Console.Write(valor_A + valor_C);
Console.Write(valor_A + valor_D);
Console.Write(valor_B + valor_C);
Console.Write(valor_B + valor_D);
Console.Write(valor_C + valor_D);

Console.Write("Valor Multiplicado");
Console.Write(valor_A * valor_B);
Console.Write(valor_A * valor_C);
Console.Write(valor_A * valor_D);
Console.Write(valor_B * valor_C);
Console.Write(valor_B * valor_D);
Console.Write(valor_C * valor_D);